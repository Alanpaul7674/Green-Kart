// EMISSION FACTORS (for calculating carbon footprint)
// These values are based on industry research
// ==============================================================================

const MATERIAL_FACTORS = {
  // High impact synthetic materials
  polyester: 5.5,
  nylon: 6.0,
  acrylic: 5.0,
  synthetic: 5.5,
  
  // Medium impact natural materials
  cotton: 2.1,
  wool: 2.5,
  linen: 1.5,
  silk: 2.8,
  leather: 3.5,
  denim: 2.3,
  
  // Low impact eco-friendly materials
  organic_cotton: 1.2,
  recycled_polyester: 1.8,
  recycled_cotton: 1.0,
  bamboo: 0.8,
  hemp: 0.7,
  recycled: 1.0,
};

const TRANSPORT_FACTORS = {
  air: 0.001095,
  road: 0.000105,
  rail: 0.000028,
  sea: 0.000016,
};

const PACKAGING_FACTORS = {
  plastic: 0.45,
  paper: 0.25,
  recycled: 0.10,
  cardboard: 0.20,
  biodegradable: 0.15,
};

// ==============================================================================
// HELPER FUNCTION: Calculate Carbon Footprint
// ==============================================================================

/**
 * Calculate carbon footprint for a product
 * @param {Object} product - Product with material, weight, distance, transport, packaging
 * @returns {Object} - Calculated carbon values
 */
function calculateCarbon(product) {
  const materialFactor = MATERIAL_FACTORS[product.materialType.toLowerCase()] || 3.0;
  const transportFactor = TRANSPORT_FACTORS[product.transportMode] || 0.000105;
  const packagingFactor = PACKAGING_FACTORS[product.packagingType] || 0.45;

  const materialImpact = parseFloat((materialFactor * product.productWeightKg).toFixed(2));
  const transportImpact = parseFloat((transportFactor * product.productWeightKg * product.distanceKm).toFixed(2));
  const packagingImpact = packagingFactor;
  const totalCarbonFootprint = parseFloat((materialImpact + transportImpact + packagingImpact).toFixed(2));

  let carbonImpactLevel;
  if (totalCarbonFootprint <= 2.0) {
    carbonImpactLevel = 'Low';
  } else if (totalCarbonFootprint <= 5.0) {
    carbonImpactLevel = 'Medium';
  } else {
    carbonImpactLevel = 'High';
  }

  const ecoScore = Math.max(0, Math.min(100, Math.round(100 * Math.exp(-2.5 * (totalCarbonFootprint / 10)))));

  return {
    totalCarbonFootprint,
    materialImpact,
    transportImpact,
    packagingImpact,
    carbonImpactLevel,
    ecoScore,
    isEcoFriendly: carbonImpactLevel === 'Low',
  };
}

