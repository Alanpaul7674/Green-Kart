/**
 * Generate Recommendations based on product attributes
 */
function generateRecommendations(product, impactLevel) {
  const recommendations = [];

  if (['polyester', 'nylon', 'acrylic', 'synthetic'].includes(product.materialType.toLowerCase())) {
    recommendations.push('Consider switching to recycled or organic materials to reduce impact by up to 70%');
  }

  if (product.transportMode === 'air') {
    recommendations.push('Switching from air to rail transport could reduce transport emissions by 97%');
  } else if (product.transportMode === 'road' && product.distanceKm > 1000) {
    recommendations.push('Rail transport produces 73% less emissions than road transport');
  }

  if (product.packagingType === 'plastic') {
    recommendations.push('Using recycled packaging instead of plastic reduces emissions by 78%');
  }

  if (impactLevel === 'Low') {
    recommendations.push('Great choice! This product uses sustainable practices');
  }

  return recommendations;
}

